public class Settings {
    /**
     * 是否显示系统文件
     */
    private boolean isShowSystemFile = false;

    // =============================================================================================
    // ====================== get、set ==============================

    /**
     * 是否显示系统文件
     */
    public boolean isShowSystemFile() {
        return isShowSystemFile;
    }

    /**
     * 是否显示系统文件
     */
    public void setShowSystemFile(boolean showSystemFile) {
        isShowSystemFile = showSystemFile;
    }
}
